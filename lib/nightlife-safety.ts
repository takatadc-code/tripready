// ===== API関数 =====

/**
 * 国コードから夜間安全情報を取得
 */
export function getNightSafetyInfo(countryCode: string): NightSafetyInfo | null {
  return NIGHT_SAFETY_DATA[countryCode] || null;
}

/**
 * 一般的な安全度ラベルを取得
 */
export function getSafetyLabel(level: 'safe' | 'moderate' | 'caution'): { text: string; color: string; bg: string } {
  switch (level) {
    case 'safe':
      return { text: '比較的安全', color: '#059669', bg: '#ECFDF5' };
    case 'moderate':
      return { text: '注意が必要', color: '#D97706', bg: '#FFFBEB' };
    case 'caution':
      return { text: '要警戒', color: '#DC2626', bg: '#FEF2F2' };
  }
}

/**
 * 利用可能な国一覧
 */
export function getNightSafetyCountries(): string[] {
  return Object.keys(NIGHT_SAFETY_DATA);
}
