/**
 * 日付をフォーマット（3/28（土）形式）
 */
export function formatDateJa(dateStr: string): string {
  const d = new Date(dateStr);
  const days = ['日', '月', '火', '水', '木', '金', '土'];
  return `${d.getMonth() + 1}/${d.getDate()}（${days[d.getDay()]}）`;
}

/**
 * 時刻を表示用にフォーマット
 */
export function formatTime(time: string, endTime?: string): string {
  if (endTime) {
    return `${time} 〜 ${endTime}`;
  }
  return time;
}

/**
 * 旅程のサマリー情報を取得
 */
export function getTimelineSummary(schedules: DaySchedule[]): {
  totalDays: number;
  totalEvents: number;
  completedEvents: number;
  upcomingEvents: TimelineEvent[];
} {
  const allEvents = schedules.flatMap(s => s.events);
  const today = new Date().toISOString().slice(0, 10);
  const now = new Date().toTimeString().slice(0, 5);

  const upcoming = allEvents
    .filter(e => !e.completed && (e.date > today || (e.date === today && e.time >= now)))
    .slice(0, 3);

  return {
    totalDays: schedules.length,
    totalEvents: allEvents.length,
    completedEvents: allEvents.filter(e => e.completed).length,
    upcomingEvents: upcoming,
  };
}
