// ===== フライト・ホテルから自動イベント生成 =====

interface FlightData {
  id: string;
  airline: string;
  flight_number?: string;
  departure_airport: string;
  arrival_airport: string;
  departure_time: string;    // ISO 8601
  arrival_time?: string;     // ISO 8601
}

interface HotelData {
  id: string;
  name: string;
  checkin_date: string;      // YYYY-MM-DD
  checkout_date: string;     // YYYY-MM-DD
  checkin_time?: string;     // HH:mm
  checkout_time?: string;    // HH:mm
  address?: string;
}

/**
 * フライト情報からタイムラインイベントを生成
 */
export function generateFlightEvents(tripId: string, flights: FlightData[]): TimelineEvent[] {
  const events: TimelineEvent[] = [];

  for (const flight of flights) {
    if (!flight.departure_time) continue; // 出発日時未設定のフライトはスキップ
    const depDate = flight.departure_time.slice(0, 10);
    const depTime = flight.departure_time.slice(11, 16) || '00:00';
    const flightLabel = flight.flight_number
      ? `${(flight.airline||'')} ${flight.flight_number}`
      : (flight.airline||'未設定');

    events.push({
      id: `auto_dep_${flight.id}`,
      tripId,
      date: depDate,
      time: depTime,
      type: 'flight_departure',
      title: `${flightLabel} 出発`,
      subtitle: `${flight.departure_airport||'---'} → ${flight.arrival_airport||'---'}`,
      location: flight.departure_airport||'',
      isAutoGenerated: true,
      sourceId: flight.id,
      completed: false,
    });

    if (flight.arrival_time) {
      const arrDate = flight.arrival_time.slice(0, 10);
      const arrTime = flight.arrival_time.slice(11, 16) || '00:00';

      events.push({
        id: `auto_arr_${flight.id}`,
        tripId,
        date: arrDate,
        time: arrTime,
        type: 'flight_arrival',
        title: `${flightLabel} 到着`,
        subtitle: `${flight.departure_airport} → ${flight.arrival_airport}`,
        location: flight.arrival_airport,
        isAutoGenerated: true,
        sourceId: flight.id,
        completed: false,
      });
    }
  }

  return events;
}

/**
 * ホテル情報からタイムラインイベントを生成
 */
export function generateHotelEvents(tripId: string, hotels: HotelData[]): TimelineEvent[] {
  const events: TimelineEvent[] = [];

  for (const hotel of hotels) {
    // チェックイン
    events.push({
      id: `auto_ci_${hotel.id}`,
      tripId,
      date: hotel.checkin_date,
      time: hotel.checkin_time || '15:00',  // デフォルト15時
      type: 'hotel_checkin',
      title: `${hotel.name} チェックイン`,
      location: hotel.name,
      address: hotel.address,
      isAutoGenerated: true,
      sourceId: hotel.id,
      completed: false,
    });

    // チェックアウト
    events.push({
      id: `auto_co_${hotel.id}`,
      tripId,
      date: hotel.checkout_date,
      time: hotel.checkout_time || '11:00', // デフォルト11時
      type: 'hotel_checkout',
      title: `${hotel.name} チェックアウト`,
      location: hotel.name,
      address: hotel.address,
      isAutoGenerated: true,
      sourceId: hotel.id,
      completed: false,
    });
  }

  return events;
}

// ===== 日別スケジュール生成 =====

/**
 * 旅行期間の日付リストを生成
 */
function getDateRange(startDate: string, endDate: string): string[] {
  const dates: string[] = [];
  const current = new Date(startDate);
  const end = new Date(endDate);

  while (current <= end) {
    dates.push(current.toISOString().slice(0, 10));
    current.setDate(current.getDate() + 1);
  }

  return dates;
}

/**
 * 全イベントを日別にグルーピングし、タイムライン順にソート
 */
export function buildDaySchedules(
  departureDate: string,
  returnDate: string | null | undefined,
  autoEvents: TimelineEvent[],
  customEvents: TimelineEvent[],
): DaySchedule[] {
  const allEvents = [...autoEvents, ...customEvents];
  // return_dateがnullの場合はdepartureDateのみ（日帰り扱い）
  const effectiveReturn = returnDate || departureDate;
  const dates = getDateRange(departureDate, effectiveReturn);

  return dates.map((date, index) => {
    const dayEvents = allEvents
      .filter(e => e.date === date)
      .sort((a, b) => a.time.localeCompare(b.time));

    return {
      date,
      dayNumber: index + 1,
      dayLabel: `${index + 1}日目`,
      events: dayEvents,
    };
  });
}

// ===== AsyncStorage 保存 =====

const TIMELINE_STORAGE_PREFIX = 'tripready_timeline_';

/**
 * カスタムイベントを保存
 */
export async function saveCustomEvents(tripId: string, events: TimelineEvent[]): Promise<void> {
  const customOnly = events.filter(e => !e.isAutoGenerated);
  await AsyncStorage.setItem(`${TIMELINE_STORAGE_PREFIX}${tripId}`, JSON.stringify(customOnly));
}

/**
 * カスタムイベントを読み込み
 */
export async function loadCustomEvents(tripId: string): Promise<TimelineEvent[]> {
  const raw = await AsyncStorage.getItem(`${TIMELINE_STORAGE_PREFIX}${tripId}`);
  return raw ? JSON.parse(raw) : [];
}

/**
 * 新しいカスタムイベントを作成
 */
export function createCustomEvent(
  tripId: string,
  date: string,
  time: string,
  type: TimelineEventType,
  title: string,
  options?: {
    endTime?: string;
    subtitle?: string;
    location?: string;
    address?: string;
    note?: string;
    guideKey?: string;
  },
): TimelineEvent {
  return {
    id: `custom_${Date.now()}_${Math.random().toString(36).slice(2, 6)}`,
    tripId,
    date,
    time,
    endTime: options?.endTime,
    type,
    title,
    subtitle: options?.subtitle,
    location: options?.location,
    address: options?.address,
    note: options?.note,
    guideKey: options?.guideKey,
    isAutoGenerated: false,
    completed: false,
  };
}

