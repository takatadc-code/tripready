// ===== 型定義 =====

export type TimelineEventType =
  | 'flight_departure'
  | 'flight_arrival'
  | 'hotel_checkin'
  | 'hotel_checkout'
  | 'sightseeing'
  | 'restaurant'
  | 'shopping'
  | 'activity'
  | 'transport'
  | 'free_time'
  | 'custom';

export interface TimelineEvent {
  id: string;
  tripId: string;
  date: string;              // YYYY-MM-DD
  time: string;              // HH:mm（24h）
  endTime?: string;          // HH:mm（終了時刻、任意）
  type: TimelineEventType;
  title: string;
  subtitle?: string;         // 補足情報
  location?: string;         // 場所名
  address?: string;          // 住所
  note?: string;             // メモ
  guideKey?: string;         // 紐づけた観光ガイドのキー（例: 'DE:frankfurt'）
  isAutoGenerated: boolean;  // フライト/ホテルから自動生成
  sourceId?: string;         // 元のフライトID or ホテルID
  completed: boolean;
}

export interface DaySchedule {
  date: string;              // YYYY-MM-DD
  dayNumber: number;         // 旅行の何日目か（1始まり）
  dayLabel: string;          // "Day 1" や "1日目"
  events: TimelineEvent[];
}

